public class Vehicle
{
    public String color;
    public String Model;
    public Vehicle(String color, String Model)
    {
        this.color = color;
        this.Model = Model;
    }
    public String getColor()
    {
        return color;
    }
    public void setColor()
    {
        this.color = color;
    }
    public String getModel()
    {
        return Model;
    }
    public void setModel()
    {
        this.Model = Model;
    }
}
